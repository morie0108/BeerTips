<template>
  <div>
  <v-container fluid>
    <breadcrumbs :add-items="addBreads" class="breadcrumbs"/>
    <div class="discription" v-for="category in getCategory" :key="category.id">
    <h1>{{ category.name }}</h1>
      <p class="copy">こちらのページでは、{{ category.name }}に関連する記事を紹介しています。</p>
    </div>

    <v-row
      justify="center"
    >
      <v-col
        cols="12"
        sm="11"
        md="10"
        xl="8"
      >
        <v-row v-if="testPost.length">
          <v-col
            v-for="content in testPost"
            :key="content.id"
            cols="12"
            sm="6"
            lg="4"
            xl="3"
          >
            <v-card
              max-width="400"
              class="mx-auto"
            >
              <v-card-title class="align-end fill-height font-weight-bold title">
                {{ content.title }}
              </v-card-title>
              <v-img
                :src="content.image.url"
                :alt="content.image.title"
                :aspect-ratio="16/9"
                max-height="200"
                class="white--text"
              >
                <v-card-text>
                  <v-chip
                    small
                    light
                    :color="categoryColor(content.category)"
                    :to="testLinkTo('categories', content.category)"
                    class="font-weight-bold"
                  >
                    {{ content.category.name }}
                  </v-chip>
                </v-card-text>
              </v-img>

              <v-card-text>
                {{ content.updatedAt }}
              </v-card-text>

              <v-list-item three-line style="min-height: unset;">
                <v-list-item-subtitle>
                  {{ content.body }}
                </v-list-item-subtitle>
              </v-list-item>

              <v-card-text>
                <template v-if="content.tags">
                  <v-chip
                    v-for="(tag) in content.tags"
                    :key="tag.id"
                    :to="testLinkTo('tags', tag)"
                    small
                    label
                    outlined
                    class="ma-1"
                  >

                    <v-icon
                      left
                      size="18"
                      color="grey"
                    >
                      mdi-label
                    </v-icon>
                    {{ tag.name }}
                  </v-chip>
                </template>
              </v-card-text>

              <v-card-actions>
                <v-spacer />
                <v-btn
                  text
                  color="black"
                  :to="testLinkTo('posts',content)"
                >
                  この記事をみる
                </v-btn>
              </v-card-actions>
            </v-card>
          </v-col>
        </v-row>
        <div v-else class="text-center">
          投稿された記事はありません。
        </div>
      </v-col>
    </v-row>
  </v-container>
  </div>
</template>

<script>
import { mapState, mapGetters } from 'vuex'
import draftChip from '~/components/posts/draftChip'

export default {
    components: {
    draftChip,
  },

  computed: {
  ...mapGetters(['testLinkTo','setEyeCatch', 'linkTo',]),

  getCategory() {
    const post = this.$store.getters.getTestCategories
    // console.log(post)
    const category = []
    Object.keys(post).forEach((key) => {
      if (post[key].id === this.$route.params.slug)
        {
          category.push(post[key])
        }
    });
    return category
  },

  testPost() {
    const post = this.$store.getters.getTestPost
    // console.log(post)
    const blog = []
    Object.keys(post).forEach((key) => {
      // console.log(post[key].category)

        if (post[key].category.id === this.$route.params.slug)
        {
          blog.push(post[key])
        }
    });
    // console.log(blog)
    return blog
  },

    addBreads () {
      return [{ icon: 'mdi-image-filter-none', text: 'カテゴリ一覧', to: '/categories' }]
    },
    ...mapState(['posts']),
    ...mapGetters(['setEyeCatch', 'draftChip', 'linkTo']),
    categoryColor () {
      return (category) => {
        switch (category.name) {
          case 'Beer': return '#FFEE58'
          case 'Brewery': return '#A7FFEB'
          case 'Shop・Service': return 'deep-purple lighten-3'
          default: return 'grey darken-3'
        }
      }
    }
  }
}
</script>


